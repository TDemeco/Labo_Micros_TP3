static uint8_t intensity = 10;

/*******************************************************************************
 *******************************************************************************
                        GLOBAL FUNCTION DEFINITIONS
 *******************************************************************************
 ******************************************************************************/

/* Function that prints message on LED display */
void printLED(const char* msg)
{
    display_text(msg, DISPLAY_SCROLL, 4);           //rutina de sevenseg
    //printf(msg);
    //printf("\n");
    return;
}

/* Function that changes LED brightness */
void changeLEDIntensity(uint8_t newIntensity)
{
    intensity = newIntensity;
    display_intensity(intensity);                   //rutina de sevenseg  
    //printf ("LED Intensity: %d\n", intensity);
    return;
}


